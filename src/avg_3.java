public class avg_3 {

  public static void avgofthree(int a,int b, int c){
    int avg = (a+b+c)/3;

    System.out.println("avg = " + avg);
  }
  public static void main(String[] args) {

    System.out.println("Please print the average of three no:");
    int a =3,b=4,c=8;

    avgofthree(a,b,c);
    
  }
}
